package TechnoElevate;

public class Swith_case {

	public static void main(String[] args) {
		int a=2;
	switch(a)
	{
	case 1:
	{
		System.out.println("hello 1");
	}
	case 2:
	{
		System.out.println("hello 2");
	}
	case 3:
	{
		System.out.println("hello 3");
		
	}
	default:
	{
	System.out.println("bye bye");	
	}
	}

	}

}
